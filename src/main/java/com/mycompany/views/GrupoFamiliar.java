    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
int id = polizasEdition.getId();
 String nombre=txtnombref.getText();
 String apellido=txtnombref1.getText();
  String cedula=txtcedulaf.getText();
   String fecha_nacimiento=txtfechaNf.getText();
    String sexo=txtSexof.getText();
     String parentezco=TxtParentescof.getText();
     
     
     if(nombre.isEmpty()){
 javax.swing.JOptionPane.showMessageDialog(this, "Debe colocar al menos un nombre. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
 return;}
 
 com.mycompany.models.familiares Familiares=isEdition ? familiaresEdition : new com.mycompany.models.familiares();

Familiares.setPoliza_id(id);
Familiares.setNombre(nombre);
Familiares.setApellido(apellido);
Familiares.setCedula(cedula);
Familiares.setFecha_nacimiento(fecha_nacimiento);
Familiares.setSexo(sexo);
Familiares.setParentezco(parentezco);

   try{
   DAOfamiliares dao= new DAOfamiliaresImpl();
   if(!isEdition)
       dao.registrar(Familiares);
       
   else 
       dao.modificar(Familiares);
        
   String succesMessage = isEdition ? "Modificado" : "Registrado";
        javax.swing.JOptionPane.showMessageDialog(this, "Familiar "+succesMessage+ " exitosamente. \n", "AVISO",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        if(!isEdition){ txtnombref.setText("");
            txtnombref1.setText("");
               txtcedulaf.setText("");
               txtfechaNf.setText("");
               txtSexof.setText("");
               TxtParentescof.setText("");}
        
   }   catch (Exception e){
 String errorMessage = isEdition ? "Modificar" : "Registrar";
        javax.swing.JOptionPane.showMessageDialog(this, "Ocurrio un error al "+errorMessage+ " el Familiar. \n", "AVISO",javax.swing.JOptionPane.INFORMATION_MESSAGE);
   }  
      
    }//GEN-LAST:event_jButton1ActionPerformed


